private void loadCategorie() {
        
        CategorieService categorieService = new CategorieService();
        for (Categorie c : categorieService.findAll()) {
            ListCategorie.addItem(c);
            
        }
    }
     private void load(){
        model.setRowCount(0);
        for (Chambre c : chambreService.findAll()) {
            model.addRow(new Object[]{c.getId(),c.getTel(),c.getCat()});
            
        }
    }
