    private void BtnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAjouterActionPerformed

        String telephone = TxtTelephone.getText();
        Categorie categorie = (Categorie) ListCategorie.getSelectedItem();
        if("".equals(telephone) || categorie.equals(null)){

            JOptionPane.showMessageDialog(this, "Veillez remplir tous les champs ");

        }
        else{
            if(chambreService.create(new Chambre(telephone,categorie))){
                JOptionPane.showMessageDialog(this, "chambre bien ajouté");
            }
        }
        load();
    }//GEN-LAST:event_BtnAjouterActionPerformed

    private void BtnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnModifierActionPerformed

        try{
            int column = 0;
            int row = ListChambre.getSelectedRow();
            int id = Integer.parseInt(ListChambre.getModel().getValueAt(row, column).toString()) ;
            Chambre chambre = chambreService.findById(id );
            chambre.setTel(TxtTelephone.getText());
            Categorie categorie = (Categorie) ListCategorie.getSelectedItem();
            chambre.setCat(categorie);

            if("".equals(TxtTelephone.getText()) ){
                JOptionPane.showMessageDialog(this, "Veillez remplir tous les champs ");
            }
            else{
                if(chambreService.update(chambre)){
                    JOptionPane.showMessageDialog(this, "chambre bien modifié");
                }

            }
        }catch(Exception ex){

        }
        load();
    }//GEN-LAST:event_BtnModifierActionPerformed

